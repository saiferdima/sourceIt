public class Icecream {
	
	private String type = "";
	private String taste = "";
	
public Icecream(){
	
}
public Icecream(String type, String taste){
	this.taste=taste;
	this.type=type;
	
}
public String getType(){
	return type;
}

public String gettaste(){
	return taste;
}
	

}
